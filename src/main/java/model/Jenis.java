package model;

public class Jenis {

    private int id;
    private String nama;

    public Jenis() {
    }

    public Jenis(int id, String nama) {
        this.id = id;
        this.nama = nama;
    }

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
}
